export function t(key, lang = 'en') {
  return translations[lang]?.[key] ?? translations['en'][key] ?? key
}

export const LANGUAGES = [
  { code: 'en', name: 'English', nativeName: 'English', dir: 'ltr' },
  { code: 'ar', name: 'Arabic', nativeName: 'العربية', dir: 'rtl' },
  { code: 'es', name: 'Spanish', nativeName: 'Español', dir: 'ltr' },
  { code: 'fr', name: 'French', nativeName: 'Français', dir: 'ltr' },
  { code: 'bn', name: 'Bangla', nativeName: 'বাংলা', dir: 'ltr' },
]

export function useLanguage() {
  const [lang, setLang] = React.useState(() => {
    return localStorage.getItem('fikr-lang') || 'en'
  })
  const setLanguage = (l) => {
    localStorage.setItem('fikr-lang', l)
    const dir = LANGUAGES.find((x) => x.code === l)?.dir || 'ltr'
    document.documentElement.setAttribute('dir', dir)
    setLang(l)
  }
  // Apply dir on mount
  React.useEffect(() => {
    const dir = LANGUAGES.find((x) => x.code === lang)?.dir || 'ltr'
    document.documentElement.setAttribute('dir', dir)
  }, [lang])
  return [lang, setLanguage]
}
